public class eleventh {
    public static void main(String[] args) {
        // Checking the pre-increment and post-increment operators
        // Taking values for a and c
       int a = 24, c = 30;
       // Taking values for b and d
       int b= ++a, d = c++;
       // Printing the values
       System.out.println(a);
       System.out.println(b);
       System.out.println(c);
       System.out.println(d);
    }
}