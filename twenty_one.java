public class twenty_one {
    public static void main(String[] args){
        // Checking the values using ternary operator (?:)
        int a = 63, b = 36;
        boolean x = (a < b)? true : false;
        int y = (a > b)? a : b;
        System.out.println(x);
        System.out.println(y);
        }
}
