public class twenty_four {
    public static void main(String[] args){
        // While Loop example
        // Print number 1 to 10
        int i = 1;
        while(i<11){
            System.out.print(i+" "); // this line prints numbers in a single line
            i++;
        }
        System.out.println(); // this line moves the cursor to the next line after printing all numbers
    }
}
