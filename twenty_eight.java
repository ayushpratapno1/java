public class twenty_eight {
    public static void main(String[] args){
        // Print **** using for loop
        for(int i=1; i<=4; i++){
            System.out.println("****");
        }
        // Print ***** using While loop
        System.out.println("Now the below pattern is printed using While Loop");
        int i = 1;
        while(i<=4){
            System.out.println("****");
            i++;
        }
    }
}
